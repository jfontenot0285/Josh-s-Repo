# vertex class represents each vertex as a vertex object
class Vertex:
    # vertex constructor takes node as input and assigns as id
    def __init__(self, node):
        self.id = node
        # adjacency dictionary tracks connected vertices
        self.adjacent = {}

    def __str__(self):
        return str(self.id) + ' adjacent: ' + str([x.id for x in self.adjacent])

    def add_neighbor(self, neighbor, weight=0):
        self.adjacent[neighbor] = weight

    # get connected vertices
    def get_connections(self):
        return self.adjacent.keys()

    # return vertex id
    def get_id(self):
        return self.id

    # take neighbor as input and return weight between vertex and that neighbor
    def get_weight(self, neighbor):
        return self.adjacent[neighbor]

# graph class contains the master list of vertices in a graph object
class Graph:
    def __init__(self):
        self.vertex_dict = {}
        self.total_vertices = 0
        self.populate_graph()

    def __iter__(self):
        return iter(self.vertex_dict.values())

    # add vertex object to the graph
    def add_vertex(self, node):
        self.total_vertices = self.total_vertices + 1
        new_vertex = Vertex(node)
        self.vertex_dict[node] = new_vertex
        return new_vertex

    # retrieve vertex dictionary at vertex n
    def get_adjacent(self, n):
        if n in self.vertex_dict:
            return self.vertex_dict[n]
        else:
            return None

    # take source and destination vertices then add the weighted edge between them
    def add_edge(self, src, dst, distance=0.0):
        if src not in self.vertex_dict:
            self.add_vertex(src)
        if dst not in self.vertex_dict:
            self.add_vertex(dst)
        self.vertex_dict[src].add_neighbor(self.vertex_dict[dst], distance)
        self.vertex_dict[dst].add_neighbor(self.vertex_dict[src], distance)

    # get all vertices in graph
    def get_vertices(self):
        return self.vertex_dict.keys()

